"use strict"; // Enable strict mode to enforce modern JavaScript standards

// Declare variables for name, age, and login status
let name = "hitesh"; // Represents the name of the user
let age = "14"; // Represents the age of the user (stored as a string)
let isLoggedIn = false; // Represents whether the user is logged in or not

// JavaScript Data Types:
// - number: Represents numeric values.
// - bigint: Represents integers of arbitrary length.
// - string: Represents textual data.
// - boolean: Represents true/false values.
// - null: Represents a standalone value, it is of type null.
// - undefined: Represents an undefined value.
// - symbol: Represents a unique value.

// Output the data types of variables age, null, and undefined
console.log(typeof age); // Output: "string"
console.log(typeof null); // Output: "object" (Note: This is a historical quirk in JavaScript)
console.log(typeof undefined); // Output: "undefined"

/*
JavaScript provides several primitive data types:
- number: Represents numeric values, including integers and floating-point numbers.
- bigint: Introduced in ES2020, represents integers of arbitrary length.
- string: Represents textual data enclosed in single ('') or double ("") quotes.
- boolean: Represents true or false values.
- null: Represents a standalone value indicating the absence of any object value. It's of type null.
- undefined: Represents a variable that has been declared but has not been assigned a value. It's of type undefined.
- symbol: Represents a unique identifier used as property keys in objects.
*/ 
