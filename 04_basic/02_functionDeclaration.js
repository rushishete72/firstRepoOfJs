/**
 * Function Declaration: Functions can be declared using the function keyword followed by the function name, a list of parameters
 * enclosed in parentheses, and the function body enclosed in curly braces. Function declarations are hoisted to the top of
 * their scope, meaning they can be called before they are declared.
 * Example:
 */
function greet(name) {
    console.log("Hello, " + name + "!");
}
